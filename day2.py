    # Grade Calculator (if-elif-else)

marks = int(input("Enter your marks: "))
if marks >= 80:
    print("Grade: A+")
elif marks >= 70:
    print("Grade: B")
elif marks >= 60:
    print("Grade: C")
elif marks >= 50:
    print("Grade: D")
else:
    print("Grade: F")
    

